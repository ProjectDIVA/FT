function getCA(ca, mode) {
	if (ca[mode] == "-") return "-";
	var list = ca[mode].split("#");
	//alert(list);
	var text = "";
	for (var i=0; i<list.length; i++) {
		var c = list[i].charAt(0);
		if (c == 'l') {
			text += "生命值：" + list[i].substr(1) + "<br>";
		} else if (c == 'n') {
			text += "[任意] ";
			var key = Number(list[i].substr(1));
			var note = key >> 8;
			var key1 = (key >> 4) & 0xf;
			var key2 = key & 0xf;
			text += note + " (" + getNote(key1) + " → " + getNote(key2) + ")<br>";
		} else if (c == 'h') {
			var idx = 1;
			var timing = "";
			if (list[i].charAt(1) == '[') {
				idx = list[i].indexOf(']') + 1;
				timing = list[i].substring(2, idx-1);
			}
			var idxd = list[i].indexOf('.');
			var keys = list[i].substring(idx, idxd).split(",");
			for (var j=0; j<keys.length; j++) {
				var key = Number(keys[j]);
				var note = key >> 4;
				if (j != 0) text += "，";
				//alert(keys[j]+", "+ note);
				text += note + " (" + getNote(key & 0xf) + ")";
			}
			if (idxd == list[i].length - 1) {
				text += " Hold 直到 MAX Bonus 结束";
			} else {
				var rem = list[i].substr(idxd + 1);
				c = rem.charAt(0);
				var key = Number(rem.substr(1));
				var note = key >> 6;
				key = getNote(key & 0x3f);
				if (c == 'r') {
					text += " Hold 直到 MAX Bonus 结束，" + note + " (" + key + ") 回复";
				} else if (c == 'b') {
					text += " 保持 Hold 直到 " + note + " (" + key + ") 回复";
				} else if (c == 'k') {
					text += " 保持 Hold 不放，并在 " + note + " 切为 " + key + " Hold";
				}
			}
			if (timing == "") {
				text += "。<br>"
			} else text += " (" + timing + ")。<br>";
		} else if (c == 't') {
			text += list[i].substr(1) + "<br>";
		} else if (c == '[') {
			var idx = list[i].indexOf(']') + 1;
			var timing = list[i].substring(1, idx-1);
			var idxc = list[i].indexOf(',');
			var key = Number(list[i].substring(idx, idxc));
			var note = key >> 4;
			text += note + " (" + getNote(key & 0xf) + ") ~ ";
			key = Number(list[i].substring(idxc + 1));
			note = key >> 4;
			text += note + " (" + getNote(key & 0xf) + ") 使用早迟判定 (" + timing + ") 以达到 MAX Bonus。<br>";
		} else {
			var idxc = list[i].indexOf(',');
			var idxe = list[i].length;
			if (idxc >= 0) {
				idxe = idxc;
			}
			var key = Number(list[i].substring(0, idxe));
			var note = key >> 8;
			var key1 = (key >> 4) & 0xf;
			var key2 = key & 0xf;
			text += note + " (" + getNote(key1) + " → " + getNote(key2) + ")";
			if (idxc < 0) text += "<br>";
			else {
				text += " 或者 ";
				key = Number(list[i].substr(idxc+1));
				var note = key >> 8;
				var key1 = (key >> 4) & 0xf;
				var key2 = key & 0xf;
				text += note + " (" + getNote(key1) + " → " + getNote(key2) + ")<br>";
			}
		}
	}
	return text;
}

function getSA(sa, mode) {
	//alert(sa);
	if (sa[mode] == "-") return "-";
	var list = sa[mode].split("#");
	//alert(list);
	var text = "";
	for (var i=0; i<list.length; i++) {
		var c = list[i].charAt(0);
		if (c == 'l') {
			text += "生命值：" + list[i].substr(1) + "<br>";
		} else if (c == 'k') {
			var key = Number(list[i].substr(1));
			var note = key >> 4;
			text += note + " 切换为 " + getNote(key & 0xf) + " Hold。<br>";
		} else if (c == 'h') {
			var idx = 1;
			var timing = "";
			if (list[i].charAt(1) == '[') {
				idx = list[i].indexOf(']') + 1;
				timing = list[i].substring(2, idx-1);
			}
			var idxd = list[i].indexOf('.');
			var keys = list[i].substring(idx, idxd).split(",");
			for (var j=0; j<keys.length; j++) {
				var key = Number(keys[j]);
				var note = key >> 4;
				if (j != 0) text += "，";
				//alert(keys[j]+", "+ note);
				text += note + " (" + getNote(key & 0xf) + ")";
			}
			if (idxd == list[i].length - 1) {
				text += " Hold 直到 MAX Bonus 结束";
			} else {
				var rem = list[i].substr(idxd + 1);
				c = rem.charAt(0);
				var key = Number(rem.substr(1));
				var note = key >> 6;
				key = getNote(key & 0x3f);
				if (c == 'r') {
					text += " Hold 直到 MAX Bonus 结束，" + note + " (" + key + ") 回复";
				} else if (c == 'b') {
					text += " 保持 Hold 直到 " + note + " (" + key + ") 回复";
				} else if (c == 'k') {
					text += " 保持 Hold 不放，并在 " + note + " 切为 " + key + " Hold";
				}
			}
			if (timing == "") {
				text += "。<br>"
			} else text += " (" + timing + ")。<br>";
		} else if (c == 'c') {
			text += "同达成率路线<br>";
		} else if (c == 't') {
			text += list[i].substr(1) + "<br>";
		} else if (c == 's') {
			var keys = list[i].substring(1).split(",");
			//alert(keys);
			for (var j=0; j<keys.length; j++) {
				var key = Number(keys[j]);
				var note = key >> 4;
				if (j != 0) text += "，";
				//alert(keys[j]+", "+ note);
				text += note + " (" + getNote(key & 0xf) + ")";
			}
			text += " Hold 直到游戏结束。<br>";
		} else if (c == '[') {
			var idx = list[i].indexOf(']') + 1;
			var timing = list[i].substring(1, idx-1);
			var idxc = list[i].indexOf(',');
			var key = Number(list[i].substring(idx, idxc));
			var note = key >> 4;
			text += note + " (" + getNote(key & 0xf) + ") ~ ";
			key = Number(list[i].substring(idxc + 1));
			note = key >> 4;
			text += note + " (" + getNote(key & 0xf) + ") 使用早迟判定 (" + timing + ") 以达到 MAX Bonus。<br>";
		}
		else text += list[i] + "<br>";
	}
	return text;
}
function getNote(n) {
	var note = "";
	var notes = ["←", "→", "△", "□", "×", "○"];
	for (var i=0; i<6; i++) {
		if (((n >> (5-i)) & 1) == 1) {
			note += notes[i];
		}
	}
	return note;
}